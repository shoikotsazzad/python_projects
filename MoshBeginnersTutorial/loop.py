#For Loop
for number in range(3): 
    #range is a built in function where you can send how many times you want to print something.
    #number is varible (like i in c++ for loop)
    print("This is New Bangladesh", number)

#second for loop
for i in range(1, 5):
    print(i) #this will print from 1 to 4

#while loop
print("While Loop started: ")

number = 100
while number > 0:
    print(number)
    number //= 2
